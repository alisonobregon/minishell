#include "../libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c_nb;

	c_nb = '0';
	if (n == -2147483648)
	{
		ft_putstr_fd("-2147483648", fd);
		return ;
	}
	if (n < 0)
	{
		n = -n;
		ft_putchar_fd('-', fd);
	}
	if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	if (n < 10 && n > -1)
	{
		c_nb = n + '0';
		ft_putchar_fd(c_nb, fd);
	}
}
